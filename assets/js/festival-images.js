/**
 * Get images for a specific festival
 * @param {string} festivalId - The festival ID
 * @returns {object} Image URLs for the festival
 */
function getFestivalImages(festivalId) {
  return FESTIVAL_IMAGES[festivalId] || FESTIVAL_IMAGES['default'];
}

/**
 * Get a specific image type for a festival
 * @param {string} festivalId - The festival ID
 * @param {string} imageType - Type: 'hero', 'heroAlt', 'card', 'cardAlt', 'location', 'thumbnail', 'gallery'
 * @returns {string|array} Image URL(s)
 */
function getFestivalImage(festivalId, imageType) {
  const images = getFestivalImages(festivalId);
  return images[imageType] || FESTIVAL_IMAGES['default'][imageType];
}

/**
 * Get website section images
 * @param {string} section - Section name: 'about', 'safety', 'contact', 'blog'
 * @param {string} imageType - Type of image within section
 * @returns {string} Image URL
 */
function getWebsiteImage(section, imageType) {
  if (WEBSITE_IMAGES[section] && WEBSITE_IMAGES[section][imageType]) {
    return WEBSITE_IMAGES[section][imageType];
  }
  return FESTIVAL_IMAGES['default'].hero;
}

/**
 * Get blog post images
 * @param {string} postId - Blog post ID
 * @param {string} imageType - Type: 'hero', 'heroAlt', 'thumbnail', 'content'
 * @returns {string|array} Image URL(s)
 */
function getBlogImage(postId, imageType) {
  if (WEBSITE_IMAGES.blogPosts[postId] && WEBSITE_IMAGES.blogPosts[postId][imageType]) {
    return WEBSITE_IMAGES.blogPosts[postId][imageType];
  }
  return WEBSITE_IMAGES.blog.hero;
}

/**
 * Get category image
 * @param {string} category - Category name
 * @returns {string} Image URL
 */
function getCategoryImage(category) {
  const categoryKey = category.toLowerCase().replace(/[^a-z]/g, '');
  return WEBSITE_IMAGES.categories[categoryKey] || FESTIVAL_IMAGES['default'].card;
}

/**
 * Get empowerment image
 * @param {string} type - Empowerment image type
 * @returns {string} Image URL
 */
function getEmpowermentImage(type) {
  return WEBSITE_IMAGES.empowerment[type] || WEBSITE_IMAGES.empowerment.celebration;
}

/**
 * Get experience image
 * @param {string} type - Experience image type
 * @returns {string} Image URL
 */
function getExperienceImage(type) {
  return WEBSITE_IMAGES.experience[type] || WEBSITE_IMAGES.experience.lights;
}

/**
 * Get destination image
 * @param {string} country - Country code or name
 * @returns {string} Image URL
 */
function getDestinationImage(country) {
  const countryKey = country.toLowerCase().replace(/[^a-z]/g, '');
  return WEBSITE_IMAGES.destinations[countryKey] || FESTIVAL_IMAGES['default'].location;
}

/**
 * Preload critical images for faster loading
 * @param {array} imageUrls - Array of image URLs to preload
 */
function preloadImages(imageUrls) {
  imageUrls.forEach(url => {
    const img = new Image();
    img.src = url;
  });
}

